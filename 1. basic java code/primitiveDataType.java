public class primitiveDataType {

public static void main(String[] args) { 
     System.out.println(" There are 8 premitive Data Types which are given below : ");
    byte n = 6;  
    // Range is -127 to 128 (1 bytes)
    System.out.println(n);

    short a = 24; // 2 bytes
    System.out.println(a);

    char ch= 'a'; // 2 bytes
    System.out.println(ch);

    int num = 234; //  4 bytes
    System.out.println(num);

    float rate =  234.34f; // 4 bytes
    System.out.println(rate);

    boolean  var = true; // 1 byte
    System.out.println(var); 

    long  bankbalance = 1000000000; // 8 bytes
    System.out.println(bankbalance);
    

    double price = 34.999999; // 8 bytes
    System.out.println(price);
    

}
    
}